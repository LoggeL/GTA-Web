function weaponShape(classId: WeaponClassId): ItemDefinition['shape'] {
  switch (classId) {
    case 'melee':
      return { width: 1, height: 3 };
    case 'pistol':
      return { width: 2, height: 2 };
    case 'smg':
      return { width: 3, height: 2 };
    case 'shotgun':
    case 'rifle':
      return { width: 5, height: 2 };
  }
}

function weaponWeight(classId: WeaponClassId): number {
  switch (classId) {
    case 'melee':
      return 1.2;
    case 'pistol':
      return 1.1;
    case 'smg':
      return 2.6;
    case 'shotgun':
      return 3.8;
    case 'rifle':
      return 3.5;
  }
}

