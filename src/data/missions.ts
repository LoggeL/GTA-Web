const STANDARD_FAIL_RESTART = {
  onPlayerDefeat: 'latest-checkpoint',
  onCriticalActorLost: 'latest-checkpoint',
  onAbandon: 'mission-start',
} as const;

function restartFallback(description: string): ObjectiveFallback {
  return { mode: 'restart-checkpoint', description };
}

function checkpoint(
  id: string,
  label: string,
  afterObjectiveId: string | null,
  district: DistrictId,
  x: number,
  z: number,
  vehicleHealthPercent?: number,
): CheckpointDefinition {
  return {
    id,
    label,
    afterObjectiveId,
    respawn: { district, x, y: 0, z },
    restore: {
      healthPercent: 100,
      armorPercent: 50,
      ...(vehicleHealthPercent === undefined ? {} : { vehicleHealthPercent }),
      refillMissionItems: true,
    },
  };
}

